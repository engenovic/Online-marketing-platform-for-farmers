							There are over 2,000 different rose varieties to lure us with their history and fragrance.
							This is because the rose, like the orchid, cross-breeds readily—a trait exploited first by nature, 
							and then by horticulturalists. Today, we can choose from old-fashioned favorites, as well as modern varieties
							that are the result of intensive breeding programs throughout the world.
							The rose is a flower with a rich past, and an exciting future.
Finding your way through the rose's large extended family can be both confusing and intimidating.
 Damasks, musks, gallicas, centifolias, hybrid perpetuals, Bourbons, hybrid teas, 
 ramblers and climbers—even the most distinguished rosarians have a difficult time determining which rose is which.
Tracing the history of a particular rose can be a fascinating adventure, but it is hardly an exact science.
 The old roses have cross-bred so many times, and so many varieties have been lost to time, that it is
 often impossible to uncover the exact parentage. If you are one of the many who become possessed by roses,
 you may eventually find it important to know the difference between a gallica and a Bourbon. But until that point, 
 our advice is not to worry about it. The important thing is to select a rose that you find beautiful, and that suits your garden.
Roses are usually grouped into one of two broad categories: old roses and modern roses. 
Old roses are those varieties discovered or developed prior to the introduction of the hybrid tea rose in 1867.
 But like everything else in the world of roses, when it comes to determining how a particular rose should be classified, it’s not always crystal clear.



