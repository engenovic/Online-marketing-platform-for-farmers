					 It is a common practice to color the skins of oranges in certain orange growing areas of the country because
					 of climatic or cultural conditions which cause the oranges to mature while still green in color.”
                        Why would orange growers do such a thing?
                           Before nights turn cool during the orange harvesting season of January through April,
						   orange skins might not be quite “orange enough” to be visually appealing in the produce aisle. 
						   Shoppers are more likely to purchase brightly colored fruits and veggies because the color is typically 
						   equated with dense nutrients and juice concentration. They may even be green, so some growers will spray their 
						   citrus with Citrus Red #2. (Here’s another way they entice us with not-so-bright fruit.)
                        Since you don’t consume the orange peel, the artificial dye shouldn’t affect the taste or texture of your citrus. 
						However, when purchasing your oranges, you also likely won’t know whether or not they’ve been dyed, because there 
						are no current regulations that force retailers to tell you.


