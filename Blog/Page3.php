							<p>There’s nothing better than plucking a ripe apple right off the tree.  While this isn’t the Okanagan, 
							there are dozens of varieties of apples that are hardy here and they are easy to grow.
                     Most people think apples are harder to grow than they actually are.  I’ll cover most of the questions 
					 that people have below so that you can plant one with confidence and enjoy your own fresh apples this summer.
 
                          Where to Plant
                     Apple trees are susceptible to powdery mildew, especially in cool, wet summers,
					 so make sure that they have plenty of morning sun to burn off dew.  They also need adequate air circulation and don’t like 
					 to be crammed into corners with other trees.
                      Apples produce best when they are wider than taller, which can be a problem with small yards. 
					  Proper pruning for apples limits vertical growth and favours large “scaffold” branches that radiate outwards from the main trunk.
                             This doesn’t need to be a deal-breaker, even if you have a small back yard.
							 Apples in the front yard will outshine everything else in May, bursting into flower while your perennials are still waking up. 
							 In a small back yard, prune the tree to encourage higher scaffold branches and you’ll be able to sit under it in a few years,
							 thus turning in into a living umbrella.
                  Plant your tree in rich, well drained soil.  I recommend adding a bag of Sea-soil or compost when you backfill to give it a good start.
 
                    Pollination
                            Apples need bees in order to yield.  Before it can bear fruit, an apple tree needs to 
							cross-pollinated with another that is close by.  The 2 trees should be different varieties of 
							apples that bloom at about the same time.  You can use ornamental crab-apples, as well.
                If they bloom at the same time (ie. early, mid, or late season) they can be planted up to 50 ft. apart. 
				If they bloom close to each other but not together, they need to be 20 ft. apart.  If their blooming times don’t overlap, 
				they won’t cross pollinate.
                  Growing apple trees can create connections between neighbours.  If your neighbour has an apple, ask him which one
				  it is and try to match the blooming time.  This will allow you to plant only 1 tree in your yard and it will benefit him, as well.  
				  More trees typically means a better yield.





