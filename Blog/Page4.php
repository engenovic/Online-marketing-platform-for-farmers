							<p>Like most young hustlers, <strong>Darius Cheruiyot</strong> of Sigowet/Soin, Kericho County has tried his hand in almost everything, 
							from being a mechanic to a driver and a boda boda man. But none of those ventures given him satisfaction like farming.
                     The 36-year-old <strong>tomato </strong>farmer shares with Smart Harvest his journey as a greenhouse tomato farmer.
                 "The idea of setting up a greenhouse for tomatoes came to mind towards the end of 2012 after a visit a cousin's farm. 
				 I was impressed by what I saw," Cheruiyot says.
                 He decided to try the green house because, at one point, he had attempted planting tomatoes in the open but
				 he suffered losses because the crops were attacked by pests and diseases.
                To set up a greenhouse, he had to sell his motor bike at Sh55,000.
               <strong>"My family gave me another Sh25,000 which I combined with the motor bike money to get a green house. 
			   I also bought polythene and drip pipes that I used in the greenhouse," he says.</strong>
                   After getting the dimensions from the local agriculture office, <strong>Cheruiyot </strong>embarked on building his greenhouse.
                     He constructed a 17m by 13m greenhouse that accommodates 1,200 plants.
                     In early 2013, he planted his first crop of tomatoes and though he did not get it right at first,
					 he has not looked back because the returns were encouraging.
                        "I had not spaced my first tomatoes well and they grew too tall, but produced less fruits. 
						I also realised that I was using too much fertiliser, which dented my finances and I had to do something," Cheruiyot says.




